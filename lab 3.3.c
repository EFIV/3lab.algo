#include <stdio.h>

int main() {
    int n;
    char *months[] = {"", "Січень", "Лютий", "Березень", "Квітень", "Травень", "Червень", "Липень", "Серпень", "Вересень", "Жовтень", "Листопад", "Грудень"};

    printf("Введіть номер місяця: ");
    scanf("%d", &n);

    if (n < 1 || n > 12) {
        printf("Введено некоректне значення\n");
    } else {
        printf("%s\n", months[n]);
    }

    return 0;
}
